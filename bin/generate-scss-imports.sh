#!/usr/bin/env bash
set -euo pipefail

THEME_NAME="${1:-WahlBaseTheme}"

THEME_SCSS_DIR="custom/plugins/${THEME_NAME}/src/Resources/app/storefront/src/scss"
PROJECT_ROOT="$(pwd)"
ABS_THEME_SCSS_DIR="$PROJECT_ROOT/$THEME_SCSS_DIR"
OUTPUT_FILE="$ABS_THEME_SCSS_DIR/_auto-imports.scss"

echo "Generating SCSS auto-imports…"
echo "Theme:           $THEME_NAME"
echo "Project root:    $PROJECT_ROOT"
echo "Theme SCSS dir:  $ABS_THEME_SCSS_DIR"
echo "Output file:     $OUTPUT_FILE"

mkdir -p "$ABS_THEME_SCSS_DIR"

# safety
if [[ ! -d "$ABS_THEME_SCSS_DIR" ]]; then
  echo "ERROR: SCSS dir not found: $ABS_THEME_SCSS_DIR" >&2
  exit 1
fi

tmpfile="$(mktemp)"
: > "$tmpfile"

cd "$ABS_THEME_SCSS_DIR"

has_real_scss() {
  local f="$1"

  perl -0777 -ne '
    s{/\*.*?\*/}{}gs;     # block comments
    s{//.*$}{}gm;         # line comments
    s/^\s+$//mg;          # whitespace-only lines
    s/^\s*\n//mg;         # empty lines
    print if /\S/;
  ' "$f" | grep -q .
}

# Exclude files that must NEVER be auto-imported
EXCLUDE_AUTO_IMPORTS="./_auto-imports.scss"
EXCLUDE_TOKENS="./base/_tokens.scss"

# find ONLY partials (_*.scss), exclude auto-imports and tokens
find . -type f -name "_*.scss" \
  ! -path "$EXCLUDE_AUTO_IMPORTS" \
  ! -path "$EXCLUDE_TOKENS" \
  | LC_ALL=C sort | while read -r file; do

  if has_real_scss "$file"; then
    import_path="${file#./}"
    import_path="${import_path%.scss}"

    # import without leading underscore in filename
    import_path="$(echo "$import_path" | sed -E 's@/_(.+)$@/\1@')"

    echo "@import \"$import_path\";" >> "$tmpfile"
  fi
done

cat > "$OUTPUT_FILE" <<EOF
/* =========================================================
   AUTO-GENERATED FILE – DO NOT EDIT
   Generated by: bin/generate-scss-imports.sh

   Theme: ${THEME_NAME}

   Imports ONLY SCSS partials that contain real content
   after stripping comments and empty lines.

   Excludes:
   - ./base/_tokens.scss (tokens are imported explicitly in base.scss)
   ========================================================= */
EOF

cat "$tmpfile" >> "$OUTPUT_FILE"
rm -f "$tmpfile"

echo "✅ Done. Auto-imports generated for ${THEME_NAME}."
